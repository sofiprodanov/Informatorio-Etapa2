a = 10
b = 3

resultado = a
print(f"Operador =: {resultado}")

a = a+b # es lo mismo que poner a += b, se puede aplicar a cualquier operador matematico
print(f"Operador +: {a}")

a = a-b # es lo mismo que poner a -= b, se puede aplicar a cualquier operador matematico
print(f"Operador -: {a}")

lista_1 = [1, 2, 3]
lista_2 = [4, 5]
lista_1 = lista_1 + lista_2 # es lo mismo que lista_1 += lista_2

print(f"Operador += {lista_1}")

